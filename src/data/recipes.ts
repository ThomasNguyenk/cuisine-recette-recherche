export const categories = Array.from(new Set(recipes.flatMap(recipe => recipe.category)));

export const cuisines = Array.from(new Set(recipes.map(recipe => recipe.cuisine)));

export const countries = Array.from(new Set(recipes.map(recipe => recipe.country)));

export function getRecipeById(id: string): Recipe | undefined {
  return recipes.find(recipe => recipe.id === id);
}

export function getRecipesByCategory(category: string): Recipe[] {
  return recipes.filter(recipe => recipe.category.includes(category));
}

export function getRecipesByCuisine(cuisine: string): Recipe[] {
  return recipes.filter(recipe => recipe.cuisine === cuisine);
}

export function getRecipesByCountry(country: string): Recipe[] {
  return recipes.filter(recipe => recipe.country === country);
}

export function searchRecipes(query: string): Recipe[] {
  const lowercaseQuery = query.toLowerCase();
  return recipes.filter(recipe => 
    recipe.title.toLowerCase().includes(lowercaseQuery) || 
    recipe.description.toLowerCase().includes(lowercaseQuery) ||
    recipe.ingredients.some(ingredient => ingredient.toLowerCase().includes(lowercaseQuery))
  );
}